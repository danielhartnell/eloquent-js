// Note: All values have a type
// Flashcard Q1: What is type coercion?
// Flashcard A1: Javascript will try to match types

// Arithemtic
console.log(100 + 4 * 11);
console.log((1 - 2) + 1);

// Special Numbers
console.log(typeof Infinity);

// Strings
console.log("Con" + "cat" + "e" + "nate");
console.log("First line\nSecond line");
console.log("Escaping a quote: \"");

// Unary Operators
// Flashcard Q2: What is a unary operator?
// Flashcard A2: An operator that uses one value
console.log(typeof "My String");

// Binary Operators
// Flashcard Q3: What is a binary operator?
// Flashcard A3: An operator that uses two values
console.log(10 + 5);

// Boolean Operators
console.log(2 > 34); // Return a boolean
console.log(NaN == NaN); // NaN is not equal to any other nonsense calculation

// Logical Operators
// Javascript supports three: and, or, not
console.log(true && false);
console.log(true && true);

// Ternary Operators
// The value to the left of the ? pick which value will be returned
console.log(true ? 1 : 2);
console.log(false ? 1 : 2);
